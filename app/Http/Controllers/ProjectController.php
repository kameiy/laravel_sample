    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Project  $project
     * @return \Illuminate\Http\Response
     */
    public function destroy(Project $project)
    {
        $store_id = $project->store->id;
        $project->delete();
        return redirect('stores/'.$store_id);
